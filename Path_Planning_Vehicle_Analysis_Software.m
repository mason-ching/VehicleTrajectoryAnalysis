axes(handles.axes1);
bar([1:length(speed)], speed, 'FaceColor', 'r', 'EdgeColor', 'none');
xlabel('时间(分钟)'); ylabel('车速 (千米/小时)'); title('车速柱状图');
xlim([0, 101]); ylim([0, 50]);
grid on;

% 绘制行驶时间折线图
yyaxis right;
plot(time, 'LineWidth', 2, 'Color', 'g');
ylabel('行驶时间 (秒)'); title('车辆运动轨迹变化曲线图');
xlim([0 101]); ylim([0 max(time)+10]);
grid on;

%添加整个图表的图例
legend('运动轨迹','车速','行驶时间','Location', 'northeast');

