cla reset;

% 随机生成车辆运动轨迹数据
x = cumsum(rand(1,100)*200-100);
y = cumsum(rand(1,100)*200-100);
speed = rand(1,100)*30 + 30;
time = zeros(size(speed));
for i = 2:length(time)
    time(i) = time(i-1) + norm([x(i)-x(i-1), y(i)-y(i-1)])/speed(i);
end

